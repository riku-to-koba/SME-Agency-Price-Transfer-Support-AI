def get_step_prompt(step: str) -> str:
    """ステップに応じた追加プロンプトを取得

    Args:
        step: ステップID（例: "STEP_0_CHECK_3"）

    Returns:
        str: 追加プロンプト（該当なしの場合は空文字）
    """
    if step and step in STEP_CONTEXT:
        return STEP_CONTEXT[step]
    return ""


